    //Este metodo sirve para llenar la tabla con los campos necesarios. Se llama desde el constructor.

    public void refreshTable() {

        DefaultTableModel tableModel = new DefaultTableModel();

        tableModel.addColumn("Id");
        tableModel.addColumn("Chofer");
        tableModel.addColumn("Fecha");
        tableModel.addColumn("Estado");
        tableModel.addColumn("Origen");
        tableModel.addColumn("Destino");
        tableModel.addColumn("Kilometros");
        tableModel.addColumn("Valor Km");
        tableModel.addColumn("Tarifa");
        tlListadoViaje.setModel(tableModel);


        for(Viaje viaje : viajeController.readAll()) {
            Object[] row = new Object[9];

            row[0] = viaje.getId();
            row[1] = viaje.getChofer().getNombre();
            row[2] = viaje.getFecha();
            row[3] = viaje.getEstadoViaje();
            row[4] = viaje.getOrigen();
            row[5] = viaje.getDestino();
            row[6] = viaje.getKilometros();
            row[7] = viaje.getValorKm();
            row[8] = viaje.getKilometros() * viaje.getValorKm();

            tableModel.addRow(row);

        }

        tlListadoViaje.setModel(tableModel);

    }

