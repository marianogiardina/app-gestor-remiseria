    private void btnEditarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarViajeActionPerformed

        //Primero verifico que haya un viaje seleccionado
        int selectedRow = tlListadoViaje.getSelectedRow();

        if (selectedRow < 0) {
            // Si no hay fila seleccionada, muestro un mensaje de error
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un viaje para editar.");
        }else{
            // Si hay fila seleccionada, obtengo el id del viaje y muestro el panel de edicion
            int viajeId = (int) tlListadoViaje.getValueAt(selectedRow, 0);

            // Muestro el panel de edicion de viaje, llamando al metodo read, que me devuelve el viaje con el id seleccionado.
            mostrarPanel(new FormViaje(true, viajeController.read(viajeId)));
        }

    }//GEN-LAST:event_btnEditarViajeActionPerformed

    private void btnBorrarViajeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarViajeActionPerformed

        int selectedRow = tlListadoViaje.getSelectedRow();
        if (selectedRow < 0) {
            // Si no hay fila seleccionada, muestro un mensaje de error
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un viaje para borrar.");
        } else {

            // Si hay fila seleccionada, obtengo el id del viaje y lo borro

            int viajeId = (int) tlListadoViaje.getValueAt(selectedRow, 0);

            // Confirmo la accion de borrado

            int confirmarAccion = JOptionPane.showConfirmDialog(this, "Â¿Desea borrar este viaje?", "Confirmar accion", JOptionPane.YES_NO_OPTION);

            if (confirmarAccion == JOptionPane.YES_OPTION) {

                // Llamo al metodo delete del controlador de viajes

                viajeController.delete(viajeId);

                // Actualizo la tabla

                refreshTable();
            }
        }

    }//GEN-LAST:event_btnBorrarViajeActionPerformed


