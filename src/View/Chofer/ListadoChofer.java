    private void btnEditarChoferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarChoferActionPerformed

        //Primero verifico que haya un chofer seleccionado

        int selectedRow = tlListadoChofer.getSelectedRow();

        if (selectedRow < 0) {

            // Si no hay fila seleccionada, muestro un mensaje de error

            JOptionPane.showMessageDialog(this, "Por favor, seleccione un chofer para editar.");

        }else{

            // Si hay fila seleccionada, obtengo el id del chofer y muestro el panel de edicion

            int choferId = (int) tlListadoChofer.getValueAt(selectedRow, 0);

            // Muestro el panel de edicion de chofer, llamando al metodo read, que me devuelve el chofer con el id seleccionado.
            mostrarPanel(new FormChofer(true, choferController.read(choferId)));
        }
    }//GEN-LAST:event_btnEditarChoferActionPerformed

    private void btnBorrarChoferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarChoferActionPerformed

        int selectedRow = tlListadoChofer.getSelectedRow();

        if (selectedRow < 0) {

            // Si no hay fila seleccionada, muestro un mensaje de error

            JOptionPane.showMessageDialog(this, "Por favor, seleccione un chofer para borrar.");

        } else {

            // Si hay fila seleccionada, obtengo el id del chofer

            int choferId = (int) tlListadoChofer.getValueAt(selectedRow, 0);

            // Confirmo la accion de borrado

            int confirmarAccion = JOptionPane.showConfirmDialog(this, "Â¿Desea borrar este chofer?", "Confirmar accion", JOptionPane.YES_NO_OPTION);

            if (confirmarAccion == JOptionPane.YES_OPTION) {

                // Llamo al metodo delete del controlador de choferes

                choferController.delete(choferId);

                // Actualizo la tabla

                refreshTable();
            }
        }
    }//GEN-LAST:event_btnBorrarChoferActionPerformed


