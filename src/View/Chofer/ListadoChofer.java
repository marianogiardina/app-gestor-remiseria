    public void refreshTable() {

        DefaultTableModel tableModel = new DefaultTableModel();

        tableModel.addColumn("Id");
        tableModel.addColumn("Nombre");
        tableModel.addColumn("DNI");
        tableModel.addColumn("Celular");
        tableModel.addColumn("Auto");
        tableModel.addColumn("Disponible");
        tlListadoChofer.setModel(tableModel);


        for(Chofer chofer : choferController.readAll()) {
            Object[] row = new Object[9];

            row[0] = chofer.getId();
            row[1] = chofer.getNombre();
            row[2] = chofer.getDni();
            row[3] = chofer.getCelular();
            row[4] = chofer.getAutoAlquilado() != null ? chofer.getAutoAlquilado().getMarca()+" "+chofer.getAutoAlquilado().getModelo() : "Tiene auto propio";
            row[5] = chofer.isDisponible() ? "Disponible" : "No Disponible";


            tableModel.addRow(row);

        }

        tlListadoChofer.setModel(tableModel);

    }

