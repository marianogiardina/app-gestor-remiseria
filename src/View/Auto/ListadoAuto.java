    private void btnEditarAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarAutoActionPerformed

        //Primero verifico que haya un auto seleccionado

        int selectedRow = tlListadoAuto.getSelectedRow();

        if (selectedRow < 0) {

            // Si no hay fila seleccionada, muestro un mensaje de error

            JOptionPane.showMessageDialog(this, "Por favor, seleccione un auto para editar.");

        }else{

            // Si hay fila seleccionada, obtengo el id del auto y muestro el panel de edicion

            int autoId = (int) tlListadoAuto.getValueAt(selectedRow, 0);

            // Muestro el panel de edicion de auto, llamando al metodo read, que me devuelve el auto con el id seleccionado.
            mostrarPanel(new FormAuto(true, autoController.read(autoId)));
        }
    }//GEN-LAST:event_btnEditarAutoActionPerformed

    private void btnBorrarAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarAutoActionPerformed

        int selectedRow = tlListadoAuto.getSelectedRow();

        if (selectedRow < 0) {

            // Si no hay fila seleccionada, muestro un mensaje de error

            JOptionPane.showMessageDialog(this, "Por favor, seleccione un auto para borrar.");

        } else {

            // Si hay fila seleccionada, obtengo el id del auto

            int autoId = (int) tlListadoAuto.getValueAt(selectedRow, 0);

            // Confirmo la accion de borrado

            int confirmarAccion = JOptionPane.showConfirmDialog(this, "Â¿Desea borrar este auto?", "Confirmar accion", JOptionPane.YES_NO_OPTION);

            if (confirmarAccion == JOptionPane.YES_OPTION) {

                // Llamo al metodo delete del controlador de autos

                autoController.delete(autoId);

                // Actualizo la tabla

                refreshTable();
            }
        }
    }//GEN-LAST:event_btnBorrarAutoActionPerformed


