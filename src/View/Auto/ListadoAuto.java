    public void refreshTable() {

        DefaultTableModel tableModel = new DefaultTableModel();

        tableModel.addColumn("Id");
        tableModel.addColumn("Marca");
        tableModel.addColumn("Modelo");
        tableModel.addColumn("Kilometraje");
        tableModel.addColumn("Disponible");
        tlListadoAuto.setModel(tableModel);


        for(Auto auto : autoController.readAll()) {
            Object[] row = new Object[5];

            row[0] = auto.getId();
            row[1] = auto.getMarca();
            row[2] = auto.getModelo();
            row[3] = auto.getKilometraje();
            row[4] = auto.isDisponible() ? "Disponible" : "No Disponible";


            tableModel.addRow(row);

        }

        tlListadoAuto.setModel(tableModel);

    }

